const month =3 
switch(month){
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("Feburary")
        break;
    case 3:
        console.log("March")
        break;
    case 4:
        console.log("April")
        break;
    case 5:
        console.log("MAy")
        break;
    case 6:
        console.log("June")
        break;
    case 7:
        console.log("july")
        break;
    case 8:
        console.log("August")
        break;
    case 9:
        console.log("September")
        break;
    default:
        console.log("Invalid month")

}
//If break is not used then it will match all the remaining cases except default remember
